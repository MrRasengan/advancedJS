// Задание 1
// Создать механизм для безопасного добавления метаданных к
// объектам книг с использованием Symbol.
// 1. Создать уникальные символы для метаданных: отзывы,
// рейтинг, теги.
// 2. Реализовать функции addMetadata (добавление метаданных)
// и getMetadata (получение метаданных).
// 3. Создать объект книги, добавить метаданные и вывести их на
// консоль.

// Создаем уникальные символы для метаданных
const metadataSymbols = {
  reviews: Symbol('отзывы'),
  rating: Symbol('рейтинг'),
  tags: Symbol('теги')
};

// Функция для добавления метаданных к объекту
function addMetadata(book, type, value) {
  // Проверяем, существует ли уже метаданное указанного типа
  if (!book[metadataSymbols[type]]) {
    // Если метаданное не существует, добавляем его
    book[metadataSymbols[type]] = value;
  } else {
    // Если метаданное уже существует, выводим предупреждение
    console.log(`Метаданные типа "${type}" уже существуют для этой книги.`);
  }
}

// Функция для получения метаданных из объекта
function getMetadata(book, type) {
  // Проверяем, существуют ли метаданные указанного типа
  if (book[metadataSymbols[type]]) {
    // Если метаданные существуют, возвращаем их
    return book[metadataSymbols[type]];
  } else {
    // Если метаданные не найдены, выводим предупреждение
    console.log(`Метаданные типа "${type}" не найдены для этой книги.`);
    return null;
  }
}

// Создаем объект книги
const book = {
  title: 'Великий Гэтсби',
  author: 'Фрэнсис Скотт Фицджеральд'
};

// Добавляем метаданные к объекту книги
addMetadata(book, 'reviews', ['Отлично', 'Захватывающе']);
addMetadata(book, 'rating', 4.5);
addMetadata(book, 'tags', ['классика', 'художественная литература']);

// Получаем метаданные из объекта книги
console.log(getMetadata(book, 'reviews')); // ['Отлично', 'Захватывающе']
console.log(getMetadata(book, 'rating')); // 4.5
console.log(getMetadata(book, 'tags')); // ['классика', 'художественная литература']
console.log(getMetadata(book, 'author')); // Метаданные типа "author" не найдены для этой книги.
