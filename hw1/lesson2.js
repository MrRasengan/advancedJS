// Задание 2
// Вы управляете рестораном, в котором работают разные повара, специализирующиеся на определенных блюдах. Клиенты приходят и делают заказы на разные блюда.
// Необходимо создать систему управления этими заказами, которая позволит:
// • Отслеживать, какой повар готовит какое блюдо.
// • Записывать, какие блюда заказал каждый клиент.
// Используйте коллекции Map для хранения блюд и их поваров, а также для хранения заказов каждого клиента. В качестве ключей для клиентов используйте объекты.

// Повара и их специализации:
// Виктор - специализация: Пицца.
// Ольга - специализация: Суши.
// Дмитрий - специализация: Десерты.

// Блюда и их повара:
// Пицца "Маргарита" - повар: Виктор.
// Пицца "Пепперони" - повар: Виктор.
// Суши "Филадельфия" - повар: Ольга.
// Суши "Калифорния" - повар: Ольга.
// Тирамису - повар: Дмитрий.
// Чизкейк - повар: Дмитрий.

// Заказы:
// Клиент Алексей заказал: Пиццу "Пепперони" и Тирамису.
// Клиент Мария заказала: Суши "Калифорния" и Пиццу "Маргарита".
// Клиент Ирина заказала: Чизкейк.

// Создаем коллекцию поваров и их специализаций
const chefSpecialties = new Map();
chefSpecialties.set('Виктор', 'Пицца');
chefSpecialties.set('Ольга', 'Суши');
chefSpecialties.set('Дмитрий', 'Десерты');

// Создаем коллекцию блюд и их поваров
const dishChefs = new Map();
dishChefs.set('Пицца "Маргарита"', 'Виктор');
dishChefs.set('Пицца "Пепперони"', 'Виктор');
dishChefs.set('Суши "Филадельфия"', 'Ольга');
dishChefs.set('Суши "Калифорния"', 'Ольга');
dishChefs.set('Тирамису', 'Дмитрий');
dishChefs.set('Чизкейк', 'Дмитрий');

// Создаем коллекцию заказов клиентов
const customerOrders = new Map();

// Добавляем заказы
const alexey = { name: 'Алексей' };
customerOrders.set(alexey, ['Пицца "Пепперони"', 'Тирамису']);

const maria = { name: 'Мария' };
customerOrders.set(maria, ['Суши "Калифорния"', 'Пицца "Маргарита"']);

const irina = { name: 'Ирина' };
customerOrders.set(irina, ['Чизкейк']);

// Функция для вывода информации о заказах
function displayOrders() {
  for (const [customer, orders] of customerOrders) {
    console.log(`Заказ ${customer.name}:`);
    for (const order of orders) {
      const chef = dishChefs.get(order);
      console.log(`- ${order} (Повар: ${chef})`);
    }
    console.log();
  }
}

// Функция для вывода информации о специализациях поваров
function displayChefSpecialties() {
  console.log('Специализации поваров:');
  for (const [chef, specialty] of chefSpecialties) {
    console.log(`${chef} - ${specialty}`);
  }
  console.log();
}

// Вызываем функции для вывода информации
displayOrders();
displayChefSpecialties();
